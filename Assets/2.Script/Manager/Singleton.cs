//UnityEngnie
using UnityEngine;

public class Singleton<T> : MonoBehaviour where T : Component
{
    private static T instance;

    public static T Instance
    {
        get
        {
            if (instance == null)
            {
                instance = FindFirstObjectByType<T>();
            }

            return instance;
        }
    }

    //상속 받아서 재정의 할 수 있다
    public virtual void Awake()
    {
        if (instance == null)
        {
            instance = this as T;

            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
